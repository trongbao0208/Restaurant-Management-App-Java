    private void jPanelTablesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelTablesMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            int x=evt.getX();
            int y=evt.getY();
            String tableName;
            tableName= JOptionPane.showInputDialog ("Enter the table's name:");
            
            if (tableName != null){
                //Create the table and set size
                ResizableTableButton newResizableTableButton = new ResizableTableButton(tableName);
                newResizableTableButton.setBounds(x, y, 70, 50);
                jPanelTables.add(newResizableTableButton);
                
                thisButtonsArrayList.add(newResizableTableButton);
                jPanelTables.revalidate();
                jPanelTables.repaint();
            }
        }
    }//GEN-LAST:event_jPanelTablesMouseClicked

