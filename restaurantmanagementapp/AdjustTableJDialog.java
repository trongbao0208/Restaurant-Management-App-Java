    public AdjustTableJDialog(GUI parent, boolean modal) {
        super(parent, modal);
        this.parent = parent;
        JPanel parentPanel = parent.getJPanelTables();
        initComponents();
        this.addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                for(int i = 0; i < thisButtonsArrayList.size(); i++){
                JButtonWithToString newJButton = new JButtonWithToString(thisButtonsArrayList.get(i).getText());
                newJButton.setBounds(thisButtonsArrayList.get(i).getBounds().x,
                                    thisButtonsArrayList.get(i).getBounds().y,
                                    thisButtonsArrayList.get(i).getBounds().width,
                                    thisButtonsArrayList.get(i).getBounds().height);
                parentButtons.add(newJButton);
        }
            }

        });
        jPanelTables.setPreferredSize(new Dimension(parentPanel.getWidth(), parentPanel.getHeight()));
        
        thisButtonsArrayList = new ArrayList<>();
        
        parentButtons = parent.getTableButtonsArrayList();
       
        
        //Recreate the table buttons 
        for(int i = 0; i< parentButtons.size(); i++){
            ResizableTableButton tableButton = new  ResizableTableButton(parentButtons.get(i).getText());
            
            tableButton.setBounds(parentButtons.get(i).getBounds().x, parentButtons.get(i).getBounds().y
                    , parentButtons.get(i).getBounds().width, parentButtons.get(i).getBounds().height);
            
            thisButtonsArrayList.add(tableButton);
            jPanelTables.add(tableButton);
        }
        
        //Clear all the table from main GUI
        parentButtons.clear();
        
        jPanelTables.revalidate();
        jPanelTables.repaint();
    }

